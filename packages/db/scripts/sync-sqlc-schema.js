import { mkdir, readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const dbPackageDir = path.resolve(scriptDir, "..");
const migrationsDir = path.resolve(dbPackageDir, "migrations");
const outputDir = path.resolve(dbPackageDir, "sqlc");
const outputFile = path.resolve(outputDir, "schema.sql");
const migrationDirPattern = /^(\d+)_.*$/;
const sourceOfTruth = "packages/db/migrations/*/migration.sql";

async function readIfExists(filePath) {
  try {
    return await readFile(filePath, "utf8");
  } catch (error) {
    if (
      error &&
      typeof error === "object" &&
      "code" in error &&
      error.code === "ENOENT"
    ) {
      return null;
    }

    throw error;
  }
}

function toRelative(filePath) {
  return path.relative(process.cwd(), filePath);
}

function assert(condition, message) {
  if (!condition) {
    throw new Error(message);
  }
}

const migrationEntries = await readdir(migrationsDir, { withFileTypes: true });
const legacySqlFiles = migrationEntries
  .filter((entry) => entry.isFile() && entry.name.endsWith(".sql"))
  .map((entry) => entry.name);

assert(
  legacySqlFiles.length === 0,
  `Legacy flat migration SQL files are no longer supported: ${legacySqlFiles.join(", ")}`,
);

const migrationDirs = migrationEntries
  .filter((entry) => entry.isDirectory() && migrationDirPattern.test(entry.name))
  .map((entry) => entry.name)
  .sort((a, b) => a.localeCompare(b));

assert(
  migrationDirs.length > 0,
  `No Drizzle migration directories found in ${toRelative(migrationsDir)}.`,
);

const migrationFiles = migrationDirs.map((dirName) => ({
  source: `${dirName}/migration.sql`,
  filePath: path.join(migrationsDir, dirName, "migration.sql"),
}));

const chunks = await Promise.all(
  migrationFiles.map(async ({ source, filePath }) => {
    const content = await readFile(filePath, "utf8");
    const normalized = content.replaceAll("\r\n", "\n").trimEnd();

    return `-- source: ${source}\n${normalized}`;
  }),
);

const header = [
  "-- AUTO-GENERATED FILE. DO NOT EDIT.",
  "-- Generated by packages/db/scripts/sync-sqlc-schema.js",
  `-- Source of truth: ${sourceOfTruth}`,
  "",
];
const mergedSchema = `${[...header, chunks.join("\n\n")].join("\n").trimEnd()}\n`;
await mkdir(outputDir, { recursive: true });
const existingOutput = await readIfExists(outputFile);

if (existingOutput === mergedSchema) {
  console.log(
    `Schema is up to date at ${toRelative(outputFile)} (${migrationFiles.length} migration file(s)).`,
  );
  process.exit(0);
}

await writeFile(outputFile, mergedSchema, "utf8");

console.log(
  `Generated ${toRelative(outputFile)} from ${migrationFiles.length} migration file(s).`,
);
